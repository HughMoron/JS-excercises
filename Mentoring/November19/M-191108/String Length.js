let string = ["Telescopes", "Glasses", "Eyes", "Monocles"];

function srtArrByStrngLngth(arr, ascending) {
    let result = arr.sort(function (a, b) {
        if (ascending) {
            return a.length - b.length;
        } else {
            return b.length - a.length;
        }
    })
    return result
}

